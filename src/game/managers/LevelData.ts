export function getSaveData(): any {
  try {
    return JSON.parse(localStorage.getItem('neonDashSave') || '{}');
  } catch {
    return {};
  }
}

export function setSaveData(data: any) {
  localStorage.setItem('neonDashSave', JSON.stringify(data));
}

export function isLevelUnlocked(id: number): boolean {
  const save = getSaveData();
  return (save.unlockedLevels || [1]).includes(id) || id === 20;
}

export function getLevelProgress(id: number): number {
  const save = getSaveData();
  return (save.levelStats || {})[id]?.bestProgress || 0;
}

export function getDifficultyStars(diff: string): number {
  const map: Record<string, number> = {
    'Easy': 1, 'Normal': 2, 'Hard': 3,
    'Harder': 4, 'Insane': 5, 'Demon': 6, 'Extreme Demon': 7
  };
  return map[diff] || 1;
}
