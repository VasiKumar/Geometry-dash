export interface LevelObstacle {
  type: 'spike' | 'spike_small' | 'spike_big' | 'platform' | 'platform_moving' | 'orb' | 'coin' | 'checkpoint' | 'portal_jump' | 'portal_gravity' | 'portal_speed';
  x: number;
  y?: number;         // Relative to ground (negative = above)
  w?: number;         // Width override
  h?: number;         // Height override
  moveY?: number;     // For moving platforms (oscillation amount)
  moveSpeed?: number; // Moving platform speed
  group?: number;     // Spike group ID
}

export interface LevelConfig {
  id: number;
  name: string;
  difficulty: 'Easy' | 'Normal' | 'Hard' | 'Harder' | 'Insane' | 'Demon' | 'Extreme Demon';
  diffColor: string;
  bgTheme: string;     // Background texture key
  color1: string;      // Primary neon color (hex)
  color2: string;      // Secondary neon color
  bpm: number;         // Beats per minute (affects pulse effects)
  baseSpeed: number;   // Player scroll speed
  obstacles: LevelObstacle[];
  length: number;      // Total level length in pixels
  stars: number;       // Max stars (1-3)
  coins: number;       // Number of secret coins
  hasCheckpoint: boolean;
  description: string;
}

// Helper to create spike groups
function spikes(startX: number, count: number, gap = 42): LevelObstacle[] {
  return Array.from({ length: count }, (_, i) => ({
    type: 'spike' as const,
    x: startX + i * gap,
  }));
}

function spikesSmall(startX: number, count: number, gap = 32): LevelObstacle[] {
  return Array.from({ length: count }, (_, i) => ({
    type: 'spike_small' as const,
    x: startX + i * gap,
  }));
}

function spikesBig(startX: number, count: number, gap = 62): LevelObstacle[] {
  return Array.from({ length: count }, (_, i) => ({
    type: 'spike_big' as const,
    x: startX + i * gap,
  }));
}

